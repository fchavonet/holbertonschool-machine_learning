#!/usr/bin/env python3

"""
Module to concatenates two arrays.
"""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Parameters:
        arr1 (list): the first array to concatenate.
        arr2 (list): the second array to concatenate.

    Returns:
        list: a new array with the elements of arr1 followed by arr2.
    """
    return arr1 + arr2
